import json
import logging
from typing import List, Optional, Dict, Any
from mcp.server.fastmcp import FastMCP

from brain.services.brain_service import BrainService
from brain.services.context_builder import ContextBuilder

# Initialize FastMCP Server
mcp = FastMCP("Project Brain Memory Server")

# Configure logging
logging.basicConfig(level=logging.INFO)
logger = logging.getLogger("mcp_brain_server")

# Instantiate Core Business Services
def get_service() -> BrainService:
    return BrainService()

def get_context_builder() -> ContextBuilder:
    return ContextBuilder()

@mcp.tool()
def register_agent(
    name: str,
    description: str,
    system_prompt: str,
    inputs: List[str] = [],
    outputs: List[str] = [],
    skills: List[str] = [],
    mcp_servers: List[str] = []
) -> str:
    """
    Registers a dynamic agent and its capabilities in the Agent Registry.
    """
    try:
        service = get_service()
        agent_data = {
            "name": name,
            "description": description,
            "status": "active",
            "system_prompt": system_prompt,
            "inputs": inputs,
            "outputs": outputs,
            "skills": skills,
            "mcp_servers": mcp_servers
        }
        res = service.register_agent(agent_data)
        return json.dumps({"status": "success", "agent": res}, indent=2)
    except Exception as e:
        logger.error(f"Error in register_agent: {e}")
        return json.dumps({"status": "error", "message": str(e)})

