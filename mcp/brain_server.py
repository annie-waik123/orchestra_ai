@mcp.tool()
def get_agent_context(session_id: str, agent_name: str) -> str:
    """
    Builds the dynamic targeted memory context for a specialist agent.
    """
    try:
        builder = get_context_builder()
        context = builder.build_context_for_agent(session_id, agent_name)
        return context
    except Exception as e:
        logger.error(f"Error in get_agent_context: {e}")
        return f"Error retrieving context for agent: {e}"

