@mcp.tool()
def update_session_state(
    session_id: str,
    active_node: Optional[str] = None,
    status: Optional[str] = None,
    git_commit_hash: Optional[str] = None,
    dag: Optional[Dict[str, Any]] = None
) -> str:
    """
    Updates the session active node, status, commit, or execution DAG.
    """
    try:
        service = get_service()
        updates = {}
        if active_node is not None:
            updates["active_node"] = active_node
        if status is not None:
            updates["status"] = status
        if git_commit_hash is not None:
            updates["git_commit_hash"] = git_commit_hash
        if dag is not None:
            updates["dag"] = dag

        res = service.update_session(session_id, updates)
        if not res:
            return json.dumps({"status": "error", "message": "Session not found"})
        return json.dumps({"status": "success", "session": res}, indent=2)
    except Exception as e:
        logger.error(f"Error in update_session_state: {e}")
        return json.dumps({"status": "error", "message": str(e)})

